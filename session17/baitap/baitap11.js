// Sử dụng hàm Math.abs()
let value1 = Math.abs(-5.5);
// Kết quả: 5.5
// Giải thích: Math.abs() trả về giá trị tuyệt đối của số được truyền vào, tức là giá trị không âm.

let value2 = Math.abs(10);
// Kết quả: 10
// Giải thích: Math.abs() trả về giá trị tuyệt đối của số được truyền vào, trong trường hợp này số đã là số dương nên không thay đổi.

// Sử dụng hàm Math.ceil()
let value3 = Math.ceil(4.4);
// Kết quả: 5
// Giải thích: Math.ceil() làm tròn số lên đến số nguyên gần nhất.

let value4 = Math.ceil(5.8);
// Kết quả: 6
// Giải thích: Math.ceil() làm tròn số lên đến số nguyên gần nhất.

// Sử dụng hàm Math.floor()
let value5 = Math.floor(5.7);
// Kết quả: 5
// Giải thích: Math.floor() làm tròn số xuống đến số nguyên gần nhất.

let value6 = Math.floor(6.1);
// Kết quả: 6
// Giải thích: Math.floor() làm tròn số xuống đến số nguyên gần nhất.

// Sử dụng hàm Math.round()
let value7 = Math.round(10.1);
// Kết quả: 10
// Giải thích: Math.round() làm tròn số đến số nguyên gần nhất, nếu phần thập phân nhỏ hơn 0.5 thì làm tròn xuống.

let value8 = Math.round(20.6);
// Kết quả: 21
// Giải thích: Math.round() làm tròn số đến số nguyên gần nhất, nếu phần thập phân lớn hơn hoặc bằng 0.5 thì làm tròn lên.